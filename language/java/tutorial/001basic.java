/*
2、JDK
aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa
	JDK是Java语言的软件开发工具包，主要用于移动设备、嵌入式设备上的java应用程序。JDK是
整个java开发的核心，它包含了JAVA的运行环境（JVM+Java系统类库）和JAVA工具。

	SE(J2SE)，standard edition，标准版，是我们通常用的一个版本，从JDK 5.0开始，改名
为Java SE。

	EE(J2EE)，enterprise edition，企业版，使用这种JDK开发J2EE应用程序，从JDK 5.0开
始，改名为Java EE。

	ME(J2ME)，micro edition，主要用于移动设备、嵌入式设备上的java应用程序，从JDK 5.0
开始，改名为Java ME。

	没有JDK的话，无法编译Java程序，如果想只运行Java程序，要确保已安装相应的JRE。
*/

